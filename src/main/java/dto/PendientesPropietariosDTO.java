package dto;

public class PendientesPropietariosDTO {

	String propietarioStr;
    String idContrato;
    String propiedadStr;
    String inquilinoStr;
    String montoStr;
    
	public String getIdContrato() {
		return idContrato;
	}
	public void setIdContrato(String idContrato) {
		this.idContrato = idContrato;
	}
	public String getPropiedadStr() {
		return propiedadStr;
	}
	public void setPropiedadStr(String propiedadStr) {
		this.propiedadStr = propiedadStr;
	}
	public String getInquilinoStr() {
		return inquilinoStr;
	}
	public void setInquilinoStr(String inquilinoStr) {
		this.inquilinoStr = inquilinoStr;
	}
	public String getMontoStr() {
		return montoStr;
	}
	public void setMontoStr(String montoStr) {
		this.montoStr = montoStr;
	}
	public String getPropietarioStr() {
		return propietarioStr;
	}
	public void setPropietarioStr(String propietarioStr) {
		this.propietarioStr = propietarioStr;
	}
    
    

}
